namespace Avalonia.ExtendedToolkit.Controls
{
    /// <summary>
    /// enum for the content
    /// </summary>
    public enum DropDownButtonContentDisplayMode
    {
        /// <summary>
        /// no seperate content
        /// </summary>
        NoContent,

        /// <summary>
        /// Selected item is displayes in a content control
        /// </summary>
        Content,

        /// <summary>
        /// Selected item is displayes in a button
        /// </summary>
        Button,

        /// <summary>
        /// Selected item is displayes in a ToggleButton
        /// </summary>
        ToggleButton,

        /// <summary>
        /// Selected item is displayes in a RepeatButton
        /// </summary>
        RepeatButton
    }
}
